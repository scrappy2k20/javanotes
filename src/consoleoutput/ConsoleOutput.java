package consoleoutput;

public class ConsoleOutput {

    //Main Method where instructions are written
    public static void main(String[] args)
    {
        System.out.println("Hello Alphaleaf!");  // print text to the terminal
        System.out.println();                    // print empty line to the terminal
        System.out.println(2022);                // print number to the terminal
        System.out.println(-1990);
        System.out.println(89.99);               // print fractional number to the terminal
        System.out.println('A');                 // print character to the terminal
    }

}
