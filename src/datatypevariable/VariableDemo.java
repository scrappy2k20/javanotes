package datatypevariable;

public class VariableDemo {

    public static void main(String[] args) {
        // Data Type:  Java gives you by default
        //                               [Title]
        //--------------------------------------
        // whole number                  int
        // floating point number         double
        // character                     char
        // text                          String
        // logic                         boolean

        // Creating a variable
        // [datatype title] [variable_name] = [data_value] ;

        int ssn = 127894;             // [data-type]: int,     [name]: ssn,       [data]: 127894
        int year = 2022;              // [data-type]: int,     [name]: year,      [data]: 2022
        double pu = 3.14159265;       // [data-type]: double,  [name]: pi,        [data]: 3.14159265
        double balance = 257.94;      // [data-type]: double,  [name]: balance,   [data]: 257.94
        char grade = 'A';             // [data-type]: char,    [name]: grade,     [data]: 'A'
        String msg = "Java";          // [data-type]: String,  [name]: msg,       [data]: "Java"
        boolean isRaining = true;     // [data-type]: boolean, [name]: isRaining, [data]: "Java"



    }
}
