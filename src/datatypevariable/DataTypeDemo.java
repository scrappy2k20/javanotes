package datatypevariable;

public class DataTypeDemo {

    public static void main(String[] args) {
        System.out.println(     12     );  // whole number
        System.out.println( 3.14159265 );  // floating point number
        System.out.println(    'F'     );  // character
        System.out.println("Hello Java");  // string
        System.out.println(    true    );  // logic - yes
        System.out.println(    false   );  // logic - no

        System.out.println( 1020 );
        // [data type] [variable name]

        int louisExamScore = 1020 ;
        System.out.println(louisExamScore);

    }
}
